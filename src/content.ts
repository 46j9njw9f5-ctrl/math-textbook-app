export function buildLocalAiAnswer(page: LessonPage, stumble: StumbleKey, question: string): AiExplanation {
  const selectedStumble = stumbleOptions.find((option) => option.key === stumble)

  return {
    conclusion: `${page.title}では「${page.catchCopy}」を軸に考えると整理しやすいです。特に「${selectedStumble?.label ?? ''}」で詰まっているなら、${page.stumbleGuides[stumble]}`,
    intuition: page.intuition,
    math: `${page.formulas[0].latex} を中心に見ると、${page.formulas[0].meaning} ${page.formulas[0].derivation}`,
    example: question.trim()
      ? `質問「${question.trim()}」に近い見方として、${page.exampleProblem} では ${page.exampleSteps[0].explanation}`
      : `${page.exampleProblem} では、${page.exampleSteps
          .slice(0, 2)
          .map((step) => step.explanation)
          .join(' ')}`,
    mistakes: page.misconceptions[0],
    check: `${page.title}に関して、${page.useCases[0]}場面を自分で 1 つ作り、まず何を読むべきか説明してみましょう。`,
    source: 'local',
  }
}
